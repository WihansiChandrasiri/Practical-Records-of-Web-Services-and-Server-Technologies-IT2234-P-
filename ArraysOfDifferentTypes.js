//Array of Strings

console.log("Array of Strings")

let name = ["cat", "dog","man"]

//Display method 1
console.log("Display method 1")
console.log(name);


// Display method 2
console.log("Display method 2 - using for loop")
for(let i=0; i<name.length; i++)
{
	console.log(name[i])
}

console.log("---------------------------------------")
console.log("\n")



//Array of characters
console.log("Array of characters")
let letters = ['a', 'b', 'c', 'd','e']
for(let j=0; j<letters.length; j++)
{
	console.log(letters[j])
}
console.log("---------------------------------------")
console.log("\n")

